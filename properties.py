class property:
    def __init__(self, name, value, rent, num_of_houses, owner):
        self.name = name
        self.value = value
        self.rent = rent
        self.num_of_houses = num_of_houses
        self.owner = owner
        
    def get_rent(property):
        int1 = property.num_of_houses
        return property.rent[int1]